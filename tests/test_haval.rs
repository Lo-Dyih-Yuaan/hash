	assert_hash!(haval_3_256, b"", "4F6938531F0BC8991F62DA7BBD6F7DE3FAD44562B8C6F4EBF146D5B4E46F7C17");
	assert_hash!(haval_3_256, b"a", "47C838FBB4081D9525A0FF9B1E2C05A98F625714E72DB289010374E27DB021D8");
	assert_hash!(haval_3_256, b"HAVAL", "91850C6487C9829E791FC5B58E98E372F3063256BB7D313A93F1F83B426AEDCC");
	assert_hash!(haval_3_256, b"0123456789", "63238D99C02BE18C3C5DB7CCE8432F51329012C228CCC17EF048A5D0FD22D4AE");
