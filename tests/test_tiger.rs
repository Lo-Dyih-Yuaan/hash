#[macro_use] mod assert;
use hash::tiger::{tiger, tiger2};
#[test]
fn test_tiger() {
	assert_hash!(tiger, b"", "3293AC630C13F0245F92BBB1766E16167A4E58492DDE73F3");
	assert_hash!(tiger, b"a", "77BEFBEF2E7EF8AB2EC8F93BF587A7FC613E247F5F247809");
	assert_hash!(tiger, b"abc", "2AAB1484E8C158F2BFB8C5FF41B57A525129131C957B5F93");
	assert_hash!(tiger, b"message digest", "D981F8CB78201A950DCF3048751E441C517FCA1AA55A29F6");
	assert_hash!(tiger, b"abcdefghijklmnopqrstuvwxyz", "1714A472EEE57D30040412BFCC55032A0B11602FF37BEEE9");
	assert_hash!(tiger, b"abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq", "0F7BF9A19B9C58F2B7610DF7E84F0AC3A71C631E7B53F78E");
	assert_hash!(tiger, b"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789", "8DCEA680A17583EE502BA38A3C368651890FFBCCDC49A8CC");
	assert_hash!(tiger, &b"1234567890".repeat(8), "1C14795529FD9F207A958F84C52F11E887FA0CABDFD91BFD");
	assert_hash!(tiger, &b"a".repeat(1000000), "6DB0E2729CBEAD93D715C6A7D36302E9B3CEE0D2BC314B41");
}

#[test]
fn test_tiger2() {
	assert_hash!(tiger2, b"", "4441BE75F6018773C206C22745374B924AA8313FEF919F41");
	assert_hash!(tiger2, b"a", "67E6AE8E9E968999F70A23E72AEAA9251CBC7C78A7916636");
	assert_hash!(tiger2, b"abc", "F68D7BC5AF4B43A06E048D7829560D4A9415658BB0B1F3BF");
	assert_hash!(tiger2, b"message digest", "E29419A1B5FA259DE8005E7DE75078EA81A542EF2552462D");
	assert_hash!(tiger2, b"abcdefghijklmnopqrstuvwxyz", "F5B6B6A78C405C8547E91CD8624CB8BE83FC804A474488FD");
	assert_hash!(tiger2, b"abcdbcdecdefdefgefghfghighijhijkijkljklmklmnlmnomnopnopq", "A6737F3997E8FBB63D20D2DF88F86376B5FE2D5CE36646A9");
	assert_hash!(tiger2, b"ABCDEFGHIJKLMNOPQRSTUVWXYZabcdefghijklmnopqrstuvwxyz0123456789", "EA9AB6228CEE7B51B77544FCA6066C8CBB5BBAE6319505CD");
	assert_hash!(tiger2, &b"1234567890".repeat(8), "D85278115329EBAA0EEC85ECDC5396FDA8AA3A5820942FFF");
	assert_hash!(tiger2, &b"a".repeat(1000000), "E068281F060F551628CC5715B9D0226796914D45F7717CF4");
}